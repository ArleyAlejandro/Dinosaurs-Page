        <h2 id="extincion">Extinción</h2>

        <p>La extinción de los dinosaurios es uno de los eventos más trascendentales en la historia de la vida en la
          Tierra
          y ha sido objeto de intenso debate y especulación científica durante décadas. Se cree que ocurrió al final del
          período Cretácico, hace aproximadamente 65 millones de años. Algunos aspectos importantes sobre este evento
          incluyen:</p>

        <ul>
          <li><strong>Impacto catastrófico:</strong> Una de las teorías más aceptadas sobre la extinción de los
            dinosaurios
            es que fue causada por el impacto de un asteroide o cometa con la Tierra. Este impacto habría generado
            enormes
            incendios forestales, tsunamis gigantes y una nube de polvo que bloqueó la luz del sol, alterando
            drásticamente
            el clima y afectando los ecosistemas globales.</li>
          <li><strong>Debate sobre la intensidad del evento:</strong> Aunque el impacto catastrófico es ampliamente
            aceptado
            como un factor contribuyente a la extinción masiva, algunos científicos también sugieren que otros factores,
            como la actividad volcánica masiva y cambios climáticos graduales, podrían haber desempeñado un papel en el
            evento de extinción.</li>
          <li><strong>Impacto en la biota:</strong> La extinción del Cretácico-Paleógeno (K-Pg) tuvo un impacto
            devastador
            en la biota terrestre y marina. No solo desaparecieron los dinosaurios no aviares, sino también muchas otras
            especies de plantas y animales, incluidos ammonites, pterosaurios y mosasaurios.</li>
          <li><strong>Consecuencias a largo plazo:</strong> La extinción de los dinosaurios permitió la radiación
            evolutiva
            de otros grupos de organismos, incluidos los mamíferos, que eventualmente se convertirían en los dominantes
            en
            los ecosistemas terrestres. Este evento también allanó el camino para la evolución y la diversificación de
            las
            aves modernas, que son los descendientes directos de los dinosaurios terópodos.</li>
        </ul>

        <p>Aunque la extinción de los dinosaurios marcó el fin de una era en la historia de la vida en la Tierra, su
          legado
          perdura en el registro fósil y en nuestro entendimiento de la evolución y la dinámica de los ecosistemas a lo
          largo del tiempo geológico.</p>
      </section>

