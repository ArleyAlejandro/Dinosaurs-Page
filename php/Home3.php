        <h2>Comportamiento y Ecología</h2>
        <p>Los dinosaurios exhibieron una amplia gama de comportamientos y se adaptaron a una variedad de nichos
          ecológicos durante su reinado en la Tierra. Algunos aspectos importantes de su comportamiento y ecología
          incluyen:</p>
        <ul>
          <li><strong>Hábitos alimenticios:</strong> Los dinosaurios tenían una variedad de dietas, incluyendo
            carnívoros, herbívoros y omnívoros. Algunos cazaban en solitario, mientras que otros vivían en manadas para
            cazar presas más grandes o defenderse de depredadores.</li>
          <li><strong>Patrones de locomoción:</strong> Los dinosaurios desarrollaron diferentes patrones de locomoción
            según su morfología y estilo de vida. Algunos eran rápidos y ágiles, mientras que otros eran más lentos y
            pesados, adaptados para caminar sobre cuatro patas.</li>
          <li><strong>Comportamiento social:</strong> Se cree que muchos dinosaurios, especialmente los terópodos y
            algunos saurópodos, exhibían comportamiento social. Se han encontrado evidencias de que algunos dinosaurios
            vivían en manadas o grupos familiares, lo que sugiere interacciones sociales complejas.</li>
          <li><strong>Interacciones ecológicas:</strong> Los dinosaurios interactuaban con otros organismos en sus
            ecosistemas, incluidos depredadores, presas y competidores. Estas interacciones jugaron un papel importante
            en la estructura y función de los ecosistemas prehistóricos.</li>
          <li><strong>Adaptaciones al ambiente:</strong> Los dinosaurios desarrollaron adaptaciones físicas y
            comportamentales para sobrevivir en una variedad de ambientes, desde selvas tropicales hasta desiertos
            áridos. Estas adaptaciones incluían variaciones en la forma del cuerpo, la estructura de los dientes y las
            estrategias de caza o alimentación.</li>
        </ul>
        <p>En conjunto, estos aspectos del comportamiento y la ecología de los dinosaurios nos proporcionan una visión
          fascinante de cómo estas criaturas antiguas interactuaban con su entorno y entre sí en un mundo prehistórico
          en constante cambio.</p>
