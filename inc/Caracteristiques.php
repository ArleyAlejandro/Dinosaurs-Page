                <h2>Clasificación</h2>

                <p>Los dinosaurios pueden clasificarse en varios grupos principales, que se basan principalmente en la
                    estructura de sus caderas. Las dos categorías principales son:</p>

                <p><a href="https://es.wikipedia.org/wiki/Saurischia" target="_blank">1. Saurischia:</a>Este grupo incluye a los dinosaurios con caderas reptilianas
                    similares a las de los lagartos. Se subdivide en dos principales subgrupos:</p>
                <ul>
                    <li>Theropoda: Dinosaurios carnívoros bipedales, como el Tyrannosaurus rex y el Velociraptor.</li>
                    <li>Sauropodomorpha: Dinosaurios herbívoros generalmente cuadrúpedos con cuellos largos y cuerpos
                        masivos, como el Brachiosaurus y el Diplodocus.</li>
                </ul>

                <p><a href="https://es.wikipedia.org/wiki/Ornithischia" target="_blank">2. Ornithischia:</a> Este grupo incluye a los dinosaurios con caderas similares a las de
                    las aves. Algunos de los subgrupos más conocidos son:</p>
                <ul>
                    <li>Cerapoda: Incluye a los ornitisquios con caderas de estructura avanzada, como el Triceratops y
                        el Stegosaurus.</li>
                    <li>Thyreophora: Dinosaurios con armaduras óseas o dérmicas, como los anquilosaurios y
                        estegosaurios.</li>
                </ul>

                <p>Estas clasificaciones son fundamentales para comprender la diversidad y evolución de los dinosaurios
                    a lo largo del tiempo geológico.</p>

            </section>

            <h2 id="top">Top 10 más populares</h2>
            <h3 id="Spinosaurus">1. Spinosaurus</h3>
            <p>Es el depredador terrestre más grande del mundo, además de ser el terópodo más grande que jamas haya
                caminado
                en la Tierra. Este extraño dinosaurio fue descubierto en 1931 y se estimó en 12 metros de largo y 6
                metros
                de alto (con la postura tripode) pero años después fue estudiado por medio de imágenes y dibujos de
                Spinosaurus y de otros Spinosauridae (ya que los restos fósiles fueron destruido en un bombardeo a la
                cuidad
                alemana de Berlín durante la Segunda Guerra Mundial) se le estimó 15,2 metros y 4 a 5 metros de alto,
                pero
                en el año 2014 después de estudiar los dibujos y huesos hallados durante ese año se dió a conocer una
                nueva
                longitud junto con una nueva postura, ahora se estima en de 15,2 a 18 metros, con 2,8 a 3 metros de alto
                y 7
                a 8 toneladas de peso. </p>
            <div class="img-cont"><img src="../IMG/spinosaurus.jpg" alt="Spinosaurus"></div>

            <h3 id="Giganotosaurus">2. Giganotosaurus</h3>
            <p>Fue considerado como el dinosaurio más grande del mundo durante 1993 y 1999 ya que se estimaba en 15,3
                metros
                de largo, pero ahora se sabe que mide 13,2 metros de largo, 3,7 metros de alto, 9,5 toneladas de peso, y
                un
                cráneo de 1,64 metros. Yo creo que Giganotosaurus hubiera llegado a los 14,2 metros porque el cráneo de
                MUCPv-95 se estima entre 1,64 y 1,74 metros, y si cuando el cráneo "medía" 1,8 metros el Giga medía 15,3
                metros, ahora si su cráneo mide 1,74 podría haber llegado por lo menos a los 14 metros de largo. Vivió
                en
                Sudamérica hace 93 a 95 millones de años. </p>
            <div class="img-cont"><img src="../IMG/Gigantosaurio.jpg" alt="Giganotosaurus"></div>

            <h3 id="Tyrannosaurus">3. Tyrannosaurus</h3>
            <p>El Tyrannosaurus rex, el más famoso de todos los dinosaurios por su gran tamaño, fue un terópodo gigante
                de
                12,3 metros de longitud, 3,8 metros de alto y un peso de 8 toneladas. A pesar de haberse considerado
                como es
                más grande de todos los dinosaurios carnívoros era superado por muchos. Hay muchas medidas exageradas
                para
                este terópodo lo cual hace pensar a la gente que era extremadamente largo y alto, por ejemplo: 15 metros
                de
                largo y 6 metros de alto o 14 metros de largo y 5 metros de alto, lo cual es poco posible.</p>
            <div class="img-cont"> <img src="../IMG/Tyrannosaurus.jpeg" alt="Tyrannosaurus"></div>

            <h3 id="Triceratops">4. Triceratops</h3>
            <p>El Triceratops es uno de los dinosaurios herbívoros más icónicos, conocido por sus tres cuernos y su
                impresionante collar óseo. Estos dinosaurios contaban con un pico, similar al de un loro, que junto con
                su
                robusta cabeza y cuello, les era de gran utilidad para cortar la vegetación dura, como plantas bajas,
                hojas
                y tallos y así alimentarse.</p>
            <div class="img-cont"> <img src="../IMG/Triceratops.jpg" alt="Triceratops"></div>

            <h3 id="Stegosaurus">5. Stegosaurus</h3>
            <p>El Stegosaurus es famoso por las grandes placas óseas a lo largo de su espalda y cola. Se cree que estas
                placas ayudaban en la regulación de su temperatura corporal. Con dientes pequeños y una boca grande, se
                especializaba en tragar grandes cantidades de vegetación sin masticar mucho, como helechos y plantas
                bajas.
            </p>
            <div class="img-cont"> <img src="../IMG/Stegosaurus.jpg" alt="Stegosaurus"></div>

            <h3 id="Ankylosaurus">6. Ankylosaurus</h3>
            <p>Este formidable herbívoro, con su cuerpo cubierto de placas óseas y un poderoso mazo en la cola, se
                defendía
                fácilmente de los depredadores. Sus dientes anchos y planos eran ideales para masticar vegetación dura,
                como
                helechos y plantas con semillas
            </p>
            <div class="img-cont"><img src="../IMG/Ankilosaurus.jpg" alt="Ankylosaurus"></div>

            <h3 id="Diplodocus">7. Diplodocus</h3>
            <p>El Diplodocus, conocido por su largo cuello y cola, tenía una dieta basada principalmente en hojas y
                brotes
                altos. Su cuello largo le permitía llegar a la vegetación más alta, mientras que su sistema digestivo
                estaba
                adaptado para procesar plantas de difícil digestión. Su dentadura indicaba una dieta de hojas suaves,
                probablemente de árboles y arbustos.
            </p>
            <div class="img-cont"> <img src="../IMG/diplodocus-dinosaur.jpg" alt="Diplodocus"></div>

            <h3 id="Suchomimus">8. Suchomimus</h3>
            <p>Suchomimus era un dinosaurio terópodo que vivió en el Cretácico Inferior (hace unos 120 millones de años)
                en lo que hoy es Níger, África. "Suchomimus" significa literalmente "imitador de cocodrilo". Sus brazos
                eran robustos y las grandes conexiones musculares indican que eran poderosos. Se alimentaba de peces y
                pequeñas presas. Hace más de 100 millones de años deambulaba por el Sáhara cuando este era un vergel
                pantanoso.
            </p>
            <div class="img-cont"> <img src="../IMG/Suchomimus.jpg" alt="Suchomimus"></div>

            <h3 id="Pterodáctilos">9. Pterodáctilos</h3>
            <p>Los tipos de reptiles voladores más conocidos son los pterodáctilos (Pterodactylus), un género de
                pterosaurios carnívoros que se alimentaban de pequeños animales. Como la mayoría de pterosaurios, los
                pterodáctilos tenían una cresta sobre la cabeza que, probablemente, era un reclamo sexual.
            </p>
            <div class="img-cont"><img src="../IMG/Pterodáctilos.jpg" alt="Pterodáctilos"></div>

            <h3 id="Archaeopteryx">10. Archaeopteryx</h3>
            <p>Es un género de aves primitivas que vivieron durante el Jurásico Superior, hace unos 150 millones de
                años. Se
                consideran una forma de transición entre los dinosaurios no voladores y las aves actuales. No medían más
                de
                medio metro, sus alas eran largas y presentaban plumas. Sin embargo, se piensa que solo podían planear y
                puede que fueran trepadores arbóreos.
            </p>
